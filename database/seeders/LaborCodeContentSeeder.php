    private function updateArticleContent(string $articleNumber, string $uzContent): void
    {
        $article = DB::table('articles')->where('article_number', $articleNumber)->first();
        if (!$article) {
            $this->command->warn("Article {$articleNumber} not found");
            return;
        }
        
        DB::table('article_translations')
            ->where('article_id', $article->id)
            ->where('locale', 'uz')
            ->update([
                'content' => $uzContent,
                'updated_at' => now(),
            ]);
            
        $this->command->info("Updated article {$articleNumber}");
